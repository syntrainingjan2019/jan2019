package test01;

public class calc {
	public double add(int a ,int b){
		return a+b;
	}
	public double add(int a,int b ,int c ){
		return a+b+c;
	}
	public double sub(int a ,int b){
		return a-b;
	}
	public double mul(int a ,int b){
		return a*b;
	}
	public double div(int a ,int b){
		return a/b;
	}
}
